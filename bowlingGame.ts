interface GameLogic {
    pinsArray: number[];
    rollBall(pins: number): void;
    calculateScore(): number;
    isStrike(rollIndex: number): boolean;
    isSpare(frameIndex: number): boolean;
    calculateBonusScore(frameIndex: number): number;
  }
  
  class BowlingGame implements GameLogic {
    static MAX_PINS = 10;
    static FRAMES = 10;
  
    pinsArray: number[];
  
    constructor() {
      this.pinsArray = [];
    }
  
    rollBall(pins: number): void {
      this.pinsArray.push(pins);
    }
  
    calculateScore(): number {
      let totalScore = 0;
      let frameIndex = 0;
  
      for (let frame = 0; frame < BowlingGame.FRAMES; frame++) {
        let frameScore = 0;
  
        if (this.isStrike(frameIndex)) {
          frameScore = BowlingGame.MAX_PINS + this.calculateBonusScore(frameIndex);
          frameIndex++;
        } else if (this.isSpare(frameIndex)) {
          frameScore = BowlingGame.MAX_PINS + this.pinsArray[frameIndex + 2];
          frameIndex += 2;
        } else {
          frameScore = this.pinsArray[frameIndex] + this.pinsArray[frameIndex + 1];
          frameIndex += 2;
        }
  
        totalScore += frameScore;
      }
  
      return totalScore;
    }
  
    isStrike(rollIndex: number): boolean {
      return this.pinsArray[rollIndex] === BowlingGame.MAX_PINS;
    }
  
    isSpare(frameIndex: number): boolean {
      const [firstRoll, secondRoll] = this.pinsArray.slice(frameIndex, frameIndex + 2);
      return firstRoll + secondRoll === BowlingGame.MAX_PINS && !this.isStrike(frameIndex);
    }
  
    calculateBonusScore(frameIndex: number): number {
      return this.pinsArray.slice(frameIndex + 1, frameIndex + 3).reduce((sum, roll) => sum + roll, 0);
    }
  }
  
  export default BowlingGame;
  



































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































